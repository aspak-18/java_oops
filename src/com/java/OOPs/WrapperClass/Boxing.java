package com.java.OOPs.WrapperClass;

public class Boxing {
    public static void main(String[] args) {
        int x=10;
        Integer y=x;
        System.out.println(y+100);
        double a=10.5;
        Double b=a;
        System.out.println(b+100);
        char c='@';
        Character ch=c;
        System.out.println(ch+100);
    }
}
