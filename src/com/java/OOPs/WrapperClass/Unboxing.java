package com.java.OOPs.WrapperClass;

public class Unboxing {
    public static void main(String[] args) {
        Integer x=18;
        int y=x;
        System.out.println(y+1000);
        Double a=15.56;
        double b=a;
        System.out.println(b+1000);
        Character c='#';
        char ch=c;
        System.out.println(ch+1000);
    }
}
